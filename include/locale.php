function GetTxtMod($m = 'p')
{
	global $txt;
	$result = '';
	switch ($m)
	{
	  case 'p':
	    $result = $txt['pm'];
	    break;
	  case 'c':
	    $result = $txt['cm'];
	    break;
	  case 'a':
	    $result = $txt['am'];
	    break;
	  case 's':
	    $result = $txt['sm'];
	    break;
	  case 'b':
	    $result = $txt['bm'];
	    break;
	  case 'e':
	    $result = $txt['em'];
	    break;
	  case 'r':
	    $result = $txt['rm'];
	    break;
	  case 'q':
	    $result = $txt['qm'];
	    break;
	  case 't':
	    $result = $txt['tm'];
	    break;
	  case 'g':
	    $result = $txt['gm'];
	    break;
	  case 'k':
	    $result = $txt['km'];
	    break;
	  case 'v':
	    $result = $txt['vm'];
	    break;
	  case 'd':
	    $result = $txt['dm'];
	    break;
	}
	return $result;
}

